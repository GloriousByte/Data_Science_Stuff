def vector_angles(X, Y):
    if len(X.shape) == 1:
        X.reshape(X.shape[0],1)

    if len(Y.shape) == 1:
        Y.reshape(Y.shape[0],1)
        


    dotprod = X*Y
    dotprod = np.sum(dotprod)
    firstmag = np.sqrt(np.square(X).sum())
    secmag = np.sqrt(np.square(Y).sum())
    res = dotprod/(firstmag * secmag)
    res = np.array(res)
    res = np.clip(res,-1.0,1.0)
    return np.degrees(np.arccos(res))

